from math import sqrt

class mathmetical_operators:
    def addition(x, y):
        print(x + y)
    
    def subtraction(x, y):
        print(x - y)
    
    def multiplication(x, y):
        print(x * y)
    
    def division(x, y):
        print(x / y)
    
    def power(x, expo):
        print(x ** expo)

    def square_root(x):
        print(sqrt(x))